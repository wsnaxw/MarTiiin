/**
 * Created by 30706 on 2018/3/13.
 */
@RestController
public class TestController {

    @Autowired
    HelloRemote helloRemote;

    @RequestMapping("/hello/{name}")
    public String index(@PathVariable("name") String name) {
        return helloRemote.hello(name);
    }
}
